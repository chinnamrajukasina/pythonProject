# Create a list
my_list = [1, 2, 3, 4, 5]

# Print the original list
print("Original List:", my_list)

# Append elements to the list
my_list.append(6)
my_list.append(7)

# Print the list after appending
print("List after Appending 6 and 7:", my_list)

# Remove elements from the list
my_list.remove(3)
my_list.remove(4)

# Print the list after removing 3 and 4
print("List after Removing 3 and 4:", my_list)
